'use client';

import { useState, Suspense } from 'react';
import Link from 'next/link';
import { useSearchParams } from 'next/navigation';
import { BookOpen } from 'lucide-react';
import { Button, Input } from '@/components/ui';
import { signUp, signInWithGoogle } from '@/app/actions/auth';

function SignupPageContent() {
  const searchParams = useSearchParams();
  const redirectTo = searchParams.get('redirect') || '/';
  const initialRole = searchParams.get('role') === 'student' ? 'student' : 'teacher';
  const [error, setError] = useState('');
  const [success, setSuccess] = useState(false);
  const [loading, setLoading] = useState(false);
  const [role, setRole] = useState<'teacher' | 'student'>(initialRole);

  async function handleSubmit(e: React.FormEvent<HTMLFormElement>) {
    e.preventDefault();
    setLoading(true);
    setError('');

    const formData = new FormData(e.currentTarget);
    formData.set('role', role);
    formData.set('redirect', redirectTo);
    const result = await signUp(formData);

    if (result?.error) {
      setError(result.error);
    } else {
      setSuccess(true);
    }
    setLoading(false);
  }

  async function handleGoogleSignup() {
    const result = await signInWithGoogle(redirectTo);
    if (result?.error) {
      setError(result.error);
    }
  }

  if (success) {
    return (
      <div className="w-full max-w-md text-center">
        <div className="bg-surface rounded-xl border border-border shadow-sm p-8">
          <div className="inline-flex items-center justify-center w-12 h-12 rounded-full bg-success-light text-success mb-4">
            <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
            </svg>
          </div>
          <h2 className="text-xl font-bold text-foreground">Check your email</h2>
          <p className="text-foreground-muted mt-2">
            We&apos;ve sent you a confirmation link. Click it to activate your account.
          </p>
          <Link href="/login" className="inline-block mt-4 text-primary hover:underline font-medium text-sm">
            Back to login
          </Link>
        </div>
      </div>
    );
  }

  return (
    <div className="w-full max-w-md">
      <div className="text-center mb-8">
        <div className="inline-flex items-center justify-center w-12 h-12 rounded-xl bg-primary text-primary-foreground mb-4">
          <BookOpen className="h-6 w-6" />
        </div>
        <h1 className="text-2xl font-bold text-foreground">Create an account</h1>
        <p className="text-foreground-muted mt-1">Get started with AI Course Creator</p>
      </div>

      <div className="bg-surface rounded-xl border border-border shadow-sm p-6 space-y-6">
        {/* Role selector */}
        <div className="flex rounded-lg border border-border overflow-hidden">
          <button
            type="button"
            onClick={() => setRole('teacher')}
            className={`flex-1 py-2.5 text-sm font-medium transition-colors ${
              role === 'teacher'
                ? 'bg-primary text-primary-foreground'
                : 'bg-surface text-foreground-muted hover:bg-surface-hover'
            }`}
          >
            Teacher
          </button>
          <button
            type="button"
            onClick={() => setRole('student')}
            className={`flex-1 py-2.5 text-sm font-medium transition-colors ${
              role === 'student'
                ? 'bg-primary text-primary-foreground'
                : 'bg-surface text-foreground-muted hover:bg-surface-hover'
            }`}
          >
            Student
          </button>
        </div>

        <Button
          variant="outline"
          className="w-full"
          onClick={handleGoogleSignup}
          type="button"
        >
          <svg className="h-5 w-5" viewBox="0 0 24 24">
            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4" />
            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
          </svg>
          Continue with Google
        </Button>

        <div className="relative">
          <div className="absolute inset-0 flex items-center">
            <div className="w-full border-t border-border" />
          </div>
          <div className="relative flex justify-center text-xs uppercase">
            <span className="bg-surface px-2 text-foreground-subtle">Or continue with email</span>
          </div>
        </div>

        <form onSubmit={handleSubmit} className="space-y-4">
          <Input
            label="Name"
            name="displayName"
            type="text"
            placeholder="Your full name"
            required
          />
          <Input
            label="Email"
            name="email"
            type="email"
            placeholder="you@example.com"
            required
            autoComplete="email"
          />
          <Input
            label="Password"
            name="password"
            type="password"
            placeholder="Create a password"
            required
            minLength={6}
            autoComplete="new-password"
          />

          {error && (
            <div className="p-3 bg-danger-light border border-danger/20 rounded-lg text-danger text-sm">
              {error}
            </div>
          )}

          <Button type="submit" className="w-full" loading={loading}>
            Create account
          </Button>
        </form>

        <p className="text-center text-sm text-foreground-muted">
          Already have an account?{' '}
          <Link href={`/login${redirectTo !== '/' ? `?redirect=${encodeURIComponent(redirectTo)}` : ''}`} className="text-primary hover:underline font-medium">
            Sign in
          </Link>
        </p>
      </div>
    </div>
  );
}

export default function SignupPage() {
  return (
    <Suspense fallback={null}>
      <SignupPageContent />
    </Suspense>
  );
}
